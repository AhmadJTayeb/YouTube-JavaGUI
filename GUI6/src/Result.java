    public void doWork() {
        String id = mainWindow.txtID.getText();
        String name = mainWindow.txtName.getText();
        double gpa = Double.parseDouble(mainWindow.txtGPA.getText());

        txtInfo.setText("Your name: " + name + ", Your ID: " + id + ", Your GPA: " + gpa);

        String grade = "F";
        if (gpa >= 4) {
            grade = "A";
        } else if (gpa >= 3) {
            grade = "B";
        } else if (gpa >= 2) {
            grade = "C";
        } else if (gpa >= 1) {
            grade = "D";
        }

        txtInfo.append(", Your grade: " + grade);
    }

